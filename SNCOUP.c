#include<stdio.h>
char s1[100000],s2[100000];
int main()
{
	int t;
	long int n,i,j,line;
	scanf("%d",&t);
	while(t)
	{
		scanf("%ld",&n);
		scanf("%s",s1);
		scanf("%s",s2);
		line=0;
		for(i=0;i<n;i++)
		{
			if(s1[i]=='*')
			break;
		}
		for(j=0;j<n;j++)
		{
			if(s2[j]=='*')
			break;
		}
		if((i==n)||(j==n));
		else
		line++;
		for(i=0;i<n;)
		{
			if((s1[i]=='.')&&(s2[i]=='.'))
			i++;
			else if((s1[i]=='*')&&(s2[i]=='*'))
			{
				line++;
				i++;
			}
			else if((s1[i]=='.')&&(s2[i]=='*'))
			{
				i++;
				while(i<n)
				{
					if(s2[i]=='*')
					{
						line++;
						break;
					}
					else
					{
						if(s1[i]=='.')
						i++;
						else
						{
							line++;
							i++;
							break;
						}
					}
				}
				if((i==n)&&(s1[i-1]!='*'))
				line++;
			}
			else
			{
				i++;
				while(i<n)
				{
					if(s1[i]=='*')
					{
						line++;
						break;
					}
					else
					{
						if(s2[i]=='.')
						i++;
						else
						{
							line++;
							i++;
							break;
						}
					}
				}
				if((i==n)&&(s2[i-1]!='*'))
				line++;				
			}			
		}
		for(i=0;i<n;i++)
		{
			if(s1[i]=='*')
			break;
		}
		for(j=0;j<n;j++)
		{
			if(s2[j]=='*')
			break;
		}
		if((i==n)&&(j==n));		
		else
		line--;
		printf("%ld\n",line);
		t--;
	}
	return 0;
}
